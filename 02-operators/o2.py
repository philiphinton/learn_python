
# Define a list containing 5 integers
values = [1, 2, 3, 4, 5]

# Check if the number 6 is in it using the membership operator ("in")
statement = 6 in values

# For printing, turn that Boolean type into a string, and make it lowercase
lowercasestatement = str(statement).lower()

print(f"It is {lowercasestatement} that 6 is in that list.")
